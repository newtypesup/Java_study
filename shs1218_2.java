package shs;

public class shs1218_2 {

	public static void main(String[] args) {
		//abs : 절대값
		
		System.out.println(Math.abs(10-7));//3
		System.out.println(Math.abs(7-10));//3
		
		//max : 최대값, min : 최소값
		
		System.out.println(Math.max(15,20));//20
		System.out.println(Math.min(-8,-2));//-8
		
		//pow : 누승, sqrt : 제곱근
		
		System.out.println(Math.pow(2,3));//8
		System.out.println(Math.sqrt(25));//5
		
		//log : 자연로그, log10 : 밑이 10인 로그
		
		System.out.println(Math.log10(100));//2.0
		System.out.println(Math.log10(10));//1.0

	}

}
